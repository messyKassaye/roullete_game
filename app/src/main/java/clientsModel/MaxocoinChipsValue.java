public class MaxocoinChipsValue {
    private String chips;
    private String maxocoin;

    public MaxocoinChipsValue(String chips, String maxocoin) {
        this.chips = chips;
        this.maxocoin = maxocoin;
    }

    public String getChips() {
        return chips;
    }

    public void setChips(String chips) {
        this.chips = chips;
    }

    public String getMaxocoin() {
        return maxocoin;
    }

    public void setMaxocoin(String maxocoin) {
        this.maxocoin = maxocoin;
    }
}
